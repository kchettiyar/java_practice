package CheckInstanceOf;

public class chekbit {
	public static void main(String[] args) {
		System.out.println(! false);
		
		System.out.println(~ 4);
		
		
		System.out.println(true & false);
		System.out.println(true | false);
		System.out.println(true ^ false);
		
		System.out.println(4 & 5);
		System.out.println(4 | 5);
		System.out.println(4 ^ 5);
		
	}
}
